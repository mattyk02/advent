class Controller {
    nacteno() {
        const demoDiv = document.getElementById("bor_dlazdice");
        for (var i = 0; i < 24; i++) {

            const cislo = document.createTextNode(i + 1);
            const btn = document.createElement("BUTTON");
            btn.name = i;
            btn.classList.add("butt")
            btn.addEventListener("click", controller.click);
            btn.addEventListener('mouseover', controller.active);
            btn.addEventListener('mouseout', controller.activeOut);



            const p = document.createElement("p");
            p.className = "cislo"


            const mesic = document.createElement("p");
            const prosinec = document.createTextNode("prosinec");
            mesic.className = "mesic"


            if (1 + parseInt(btn.name) <= model.hodina) {
                btn.classList.add("butt-otevreno")
                const play = document.createElement("img");
                play.src = "img/play.png";
                play.className = "play"
                play.id = btn.name;
                btn.style.backgroundImage = model.dny[i].nahledovy_obr
                btn.appendChild(play);



            } else {
                btn.classList.add("butt-zavreno")


                const nastroj = document.createElement("img");
                nastroj.className = "nastroj"
                nastroj.src = model.dny[i].ikona;
                nastroj.alt = model.dny[i].alt_text;
                btn.appendChild(nastroj);
            }

            p.appendChild(cislo);
            mesic.appendChild(prosinec);
            demoDiv.appendChild(btn);
            btn.appendChild(p);
            btn.appendChild(mesic);
        }




    }





    click(event) {
        model.hodnota = event.currentTarget.name;
        if (1 + parseInt(model.hodnota) <= model.hodina) {
            for (let i = 0; i < model.dny.length; i++) {
                if (model.hodnota == i) {
                    window.open(model.dny[i].odkaz, "_system");
                    break;
                }
            }
        }
    }

    active(event) {
        const btn = event.currentTarget;
        const btn_name = event.currentTarget.name;
        if (parseInt(btn_name) + 1 <= model.hodina) {
            btn.classList.remove("butt-otevreno")
            btn.classList.add("butt_active")

            const play = document.getElementById(event.currentTarget.name);
            play.src = "img/play_active.png";
            play.className = "play"
            btn.appendChild(play);
        }
    }

    activeOut(event) {
        const btn = event.currentTarget;
        const btn_name = event.currentTarget.name;
        if (parseInt(btn_name) + 1 <= model.hodina) {
            btn.classList.remove("butt_active")
            btn.classList.add("butt-otevreno")

            const play = document.getElementById(event.currentTarget.name);
            play.src = "img/play.png";
            play.className = "play"
            btn.appendChild(play);
        }
    }

}

